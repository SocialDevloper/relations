     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $video = Video::find($request->videoPost);  
 
        $comment = new Comment;
        $comment->comment_body = $request->comment;
        $comment->commentable_id = $video->id;
        $comment->commentable_type = get_class($video);
         
        $video->comments()->save($comment);
        return redirect()->route('videos.index')->with('success','Video Comment Inserted !!');
    }

